#include "../inc.h"

class Solution {
   public:
    vector<string> generateParenthesis(int n) {}
};

void test(int n, vector<string>&& expect) {
    save4print(n);
    auto ret = Solution().generateParenthesis(n);
    std::sort(expect.begin(), expect.end());
    std::sort(ret.begin(), ret.end());
    assert_eq_ret(expect, ret);
}

int main() {
    test(1, {"()"});
    test(2, {"(())", "()()"});
    test(3, {"((()))", "(()())", "(())()", "()(())", "()()()"});
    test(4, {"(((())))", "((()()))", "((())())", "((()))()", "(()(()))", "(()()())", "(()())()", "(())(())", "(())()()",
             "()((()))", "()(()())", "()(())()", "()()(())", "()()()()"});
    return 0;
}

