// 这样倒也不是不行，就是效率差点，前面就nlogn了，后面还有个跑不掉的n
class Solution {
   public:
    bool carPooling(vector<vector<int>>& trips, int capacity) {
        sort(trips.begin(), trips.end(),
             [](const vector<int>& lhs, const vector<int>& rhs) { return lhs[1] < rhs[1]; });
        map<int, int> q;
        int curr = 0;
        for (const auto& t : trips) {
            int np = t[0], from = t[1], to = t[2];
            while (!q.empty() && q.begin()->first <= from) {
                curr -= q.begin()->second;
                q.erase(q.begin());
            }
            if (curr + np > capacity) return false;
            curr += np;
            q[to] += np;
        }
        return true;
    }
};

void test(vector<vector<int>>&& trips, int capacity, bool expect) {
    save4print(trips, capacity);
    assert_eq_ret(expect, Solution().carPooling(trips, capacity));
}

