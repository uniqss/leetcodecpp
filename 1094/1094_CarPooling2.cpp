// 差分数组，妙啊
class Solution {
   public:
    bool carPooling(vector<vector<int>>& trips, int capacity) {
        int tomax = 0, curr = 0;
        for (const auto& t : trips) tomax = max(tomax, t[2]);
        vector<int> diff(tomax + 1);
        for (const auto& t : trips) diff[t[1]] += t[0], diff[t[2]] -= t[0];
        for (int i = 0; i <= tomax; ++i) {
            if (diff[i] != 0) {
                curr += diff[i];
                if (curr > capacity) return false;
            }
        }
        return true;
    }
};

void test(vector<vector<int>>&& trips, int capacity, bool expect) {
    save4print(trips, capacity);
    assert_eq_ret(expect, Solution().carPooling(trips, capacity));
}

